# @Prescription: 比较运算符和逻辑运算符.

a = 15
b = 30

# 比较运算符.
print(a == b)
print(a != b)
print(a > b)
print(a < b)
print(a >= b)
print(a <= b)

# 逻辑运算符.
x = True
y = False
print(bool(x or y))
print(bool(x and y))
print(bool(not x))
